public class ejercicio1 {
    public static void main(String[] args) {
//        Ejercicio 1: Contador de números pares
//        Itera sobre los números del 1 al 50 e imprime solo los pares.
        for (int i = 1; i <= 50; i++) {
            if (i % 2 == 0){
                System.out.println(i);
            }
        }
    }
}
